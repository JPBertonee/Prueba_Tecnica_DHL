SELECT
    CASE
        WHEN TRY_CAST(REPLACE(CHARGEABLE_WEIGHT, ',', '') AS FLOAT) <= 50 THEN '0-50 Kg'
        WHEN TRY_CAST(REPLACE(CHARGEABLE_WEIGHT, ',', '') AS FLOAT) <= 500 THEN '50-500 Kg'
        WHEN TRY_CAST(REPLACE(CHARGEABLE_WEIGHT, ',', '') AS FLOAT) <= 1000 THEN '500-1000 Kg'
        WHEN TRY_CAST(REPLACE(CHARGEABLE_WEIGHT, ',', '') AS FLOAT) <= 5000 THEN '1000-5000 Kg'
        WHEN TRY_CAST(REPLACE(CHARGEABLE_WEIGHT, ',', '') AS FLOAT) <= 10000 THEN '5000-10000 Kg'
        WHEN TRY_CAST(REPLACE(CHARGEABLE_WEIGHT, ',', '') AS FLOAT) <= 20000 THEN '10000-20000 Kg'
        ELSE 'Above 20000 Kg'
    END AS Weight_Bracket,
    COUNT(DISTINCT SHIPMENT_ID) AS Shipment_Count
FROM dbo.Details
WHERE SHIPMENT_ID IS NOT NULL
  AND TRY_CAST(REPLACE(CHARGEABLE_WEIGHT, ',', '') AS FLOAT) IS NOT NULL
GROUP BY
    CASE
        WHEN TRY_CAST(REPLACE(CHARGEABLE_WEIGHT, ',', '') AS FLOAT) <= 50 THEN '0-50 Kg'
        WHEN TRY_CAST(REPLACE(CHARGEABLE_WEIGHT, ',', '') AS FLOAT) <= 500 THEN '50-500 Kg'
        WHEN TRY_CAST(REPLACE(CHARGEABLE_WEIGHT, ',', '') AS FLOAT) <= 1000 THEN '500-1000 Kg'
        WHEN TRY_CAST(REPLACE(CHARGEABLE_WEIGHT, ',', '') AS FLOAT) <= 5000 THEN '1000-5000 Kg'
        WHEN TRY_CAST(REPLACE(CHARGEABLE_WEIGHT, ',', '') AS FLOAT) <= 10000 THEN '5000-10000 Kg'
        WHEN TRY_CAST(REPLACE(CHARGEABLE_WEIGHT, ',', '') AS FLOAT) <= 20000 THEN '10000-20000 Kg'
        ELSE 'Above 20000 Kg'
    END
ORDER BY
    MIN(TRY_CAST(REPLACE(CHARGEABLE_WEIGHT, ',', '') AS FLOAT));
GO